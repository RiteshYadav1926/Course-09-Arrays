public class Arrays {
    public static void main(String[] args) {

        int[] myIntArray = new int[10];
        myIntArray[0] = 45;
        myIntArray[5] = 10;

        double[] myDoubleArray = new double[6];
        myDoubleArray[3] = 9.578;

        System.out.println(myIntArray[0]);
        System.out.println(myDoubleArray[3]);
    }
}
